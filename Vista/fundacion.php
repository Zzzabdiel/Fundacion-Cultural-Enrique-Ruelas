    <section class="contenedor2" id="contenedor2">
        <h1 class="titulo2">¿Quiénes somos?</h1>
        <p class="texto2">La <b class="azul">Fundación Cultural Enrique Ruelas</b> surge con el propósito de
            conservar, difundir y
            enriquecer el legado de <b class="azul">Enrique Ruelas Espinosa</b>, maestro y director de teatro.
            Asimismo, para impulsar
            nuevos proyectos culturales, particularmente en el Arte Dramático, e incidir en la sociedad a través de
            ellos.

            La fundación nace en 2005 por iniciativa del <b class="azul">Dr. Enrique Ruelas Barajas</b>, hijo único
            del matrimonio entre
            <b class="azul">Enrique Ruelas</b> y <b class="azul">Alicia Barajas</b>. A la muerte de éstos,
            reconoce la necesidad de preservar el legado del
            maestro Ruelas como fundador de la Escuela de Arte Dramático en la UNAM y como director de teatro. Sus
            puestas en escena en la Ciudad de México fueron pioneras en la tradición teatral del país y representan la
            vanguardia de su época. Asimismo, su montaje de Los entremeses de Miguel de Cervantes, en Guanajuato hoy
            profundamente arraigado a la ciudad y a sus habitantes por generaciones, dio origen al Festival
            Internacional Cervantino; obra que, con las subsecuentes, impulsó el desarrollo de la vida cultural de
            Guanajuato.

            Así, la fundación ha impulsado y arropado distintos proyectos con el objetivo de conformar un crisol donde
            se unan la sociedad y las instituciones a través del arte, manteniendo vivo el fuego de lo que el propio
            Ruelas definió como "La gran experiencia humana".
        </p>
    </section>
    <section class="contenedor" id="contenedor">
        <h1 class="titulo">Misión, Visión y Objetivos</h1>
        <h1 class="titulo3">Misión</h1>
        <p class="texto">Preservar, enriquecer y difundir el legado artístico del maestro <b class="fundacion">Enrique
                Ruelas Espinosa</b> y la
            tradición cultural de Guanajuato para proyectar, a través del teatro y de las artes, los valores
            fundamentales del espíritu humano.</p>
        <h1 class="titulo3">Visión</h1>
        <p class="texto">La Fundación Cultural Enrique Ruelas, A.C. es reconocida como una asociación filantrópica
            ejemplar por su creatividad y su impulso innovador a favor de la cultura, como un crisol donde convergen la
            sociedad, la universidad y el gobierno </p>
        <h1 class="titulo3">Objetivos</h1>
        <p class="texto"><b class="fundacion">La Fundación Cultural Enrique Ruelas</b> se ha comprometido a fomentar y enriquecer el panorama
            cultural a través de una serie de objetivos claros y definidos. Estos objetivos no solo buscan honrar el
            legado del maestro <b class="fundacion">Enrique Ruelas Espinosa</b>, sino también impulsar el desarrollo artístico y social en
            diversas dimensiones. A continuación, presentamos los principales objetivos de nuestra fundación:
        <ul class="texto">

            <li>Impulsar la apreciación de las artes escénicas y otras manifestaciones artísticas para la formación de
                públicos, propiciando mecanismos para formar sociedades más sensibles.</li>
            <li>Colaborar de modo estrecho con la Universidad de Guanajuato y con los sectores público, educativo y
                privado
                en proyectos transversales que contribuyan a resarcir los estragos de la violencia en la sociedad,
                fortaleciendo así el tejido social.</li>
            <li> Fomentar en los jóvenes la creatividad a través del teatro y la literatura como vehículo de
                desarrollo.
                Contribuir, con la Universidad de Guanajuato, a impulsar la tradición teatral y fomentar, a partir de
                ella,
                la vanguardia.</li>
            <li> Impulsar el redescubrimiento de escenarios naturales, como espacios para sensibilizar a las
                comunidades con
                su entorno.</li>
        </ul>
        </p>

