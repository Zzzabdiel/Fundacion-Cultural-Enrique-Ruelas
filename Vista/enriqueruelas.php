    <section class="contenedor2" id="contenedor2">
    <p class="titulo2">Primeros Años y Formación</p>
    <p class="texto2">
        Enrique Ruelas Espinosa nació en la ciudad de Pachuca, Hidalgo, el 20 de octubre de 1913. Su
        vocación artística se manifestó desde sus primeros años. Concluidos sus estudios de
        bachillerato en Pachuca, se mudó a la Ciudad de México para estudiar Derecho. No obstante,
        pasó todo ese tiempo en la vida cultural, en los teatros y carpas de la Ciudad de México,
        perdiendo así todo el año escolar. Como castigo, fue enviado por sus padres a Guanajuato a
        estudiar la carrera de Derecho, en el año de 1936. Durante su tiempo como estudiante en esa
        ciudad organizó un grupo de teatro con sus compañero y amigos. En 1938 y 1940 presentó El
        cuatrigémino, El verdugo de Sevilla, Morena clara y No basta ser madre.
    </p>
    <p class="texto2">
        Al terminar sus estudios, en 1941, regresó a la Ciudad de México. Poco tiempo después, él y
        Fernando Wagner propusieron al Director de la Escuela Nacional Preparatoria, dar clases de
        actuación y práctica teatral. La propuesta fue aceptada y en 1942 iniciaron los primeros cursos.
        Como fruto de esas clases, se presentó en ese año en el Palacio de Bellas Artes A ninguna de
        las tres, de Fernando Calderón y al año siguiente Contigo pan y cebolla, de Manuel Eduardo de
        Gorostiza.
    </p>
</section>

<section class="contenedor1" id="contenedor1">
    <p class="titulo">Inicios en la Enseñanza Teatral</p>
    <p class="texto">
        En 1944 Julio Jiménez Rueda invitó a Enrique Ruelas y a Fernando Wagner a impartir las
        clases de teatro en la carrera de letras, éstas, junto con las de Rodolfo Usigli fueron el origen
        de la carrera de Arte Dramático en la UNAM, posteriormente Colegio de Literatura Dramática y
        Teatro.
    </p>
    <p class="texto">
        En 1944 se presentó en la Facultad de Filosofía y Letras La Locandiera, de Carlo Goldoni; en
        1949, Muerto sin sepultura de Jean Paul Sartre; en 1950 El tiempo es sueño de Henry R.
        Lenormand, La anunciación de María de Paul Claudel y La soga de Patrick Hamilton. En 1950,
        Ruelas presentó en Bellas Artes El emperador Jones de Eugene O’Neil, montaje que le valió en
        ese año el primer premio que otorgan los críticos teatrales en México al Mejor Director de
        Escena, superando a Salvador Novo y a Luis G. Basurto. Este montaje causó una gran
        impresión en el público, entre otras cosas, porque todos los actores eran de raza negra.
    </p>
</section>

<section class="contenedor2" id="contenedor2">
    <p class="titulo2">Fundación del Teatro Universitario</p>
    <p class="texto2">
        En 1952 Antonio Torres Gómez, Rector de la Universidad de Guanajuato, invitó a Ruelas para
        que fundara la carrera de Arte Dramático. Ese mismo año, con los alumnos de la carrera
        presentó Arsénico y encaje de Joseph Keserling. Asimismo, el Rector le solicitó que preparar
        un programa para el año siguiente como parte de la Asamblea Nacional de Universidades e
        Institutos de Enseñanza Superior (ANUIES). Para esta ocasión, retomó la idea de años atrás
        de hacer un homenaje a Miguel de Cervantes, con motivo de los 400 años de su nacimiento.
    </p>
    <p class="texto2">
        Fue así como nació la idea de un montaje al aire libre y una adaptación de algunos
        Entremeses: La guarda cuidadosa, El retablo de las maravillas y Los habladores. Ruelas
        adaptó y entretejió los textos y Armando Olivares Carrillo adaptó un prólogo y epílogo. El
        nombre original de espectáculo fue Entremeses de Miguel de Cervantes, su mundo imaginario
        y realidad de su mundo. El escenario natural donde se realizaron fue la Plazuela de San
        Roque. Así surgió el Teatro Universitario de Guanajuato, agrupación formada por gente del
        pueblo, estudiantes y profesores y que, a sus más de 70 años, sigue vivo.
    </p>
</section>

<section class="contenedor1" id="contenedor1">
    <p class="titulo">Impacto y Legado Cultural</p>
    <p class="texto">
        Los Entremeses cervantinos fueron estrenados el 20 de febrero de 1953. Esto trascendió en el
        tiempo, sembrando otros proyectos a lo largo de los años, como fue El Festival Internacional
        Cervantino, el Museo Iconográfico del Quijote y el Coloquio Cervantino Internacional, los dos
        últimos gracias al auspicio de don Eulalio Ferrer; lo que en 2005 le valdría a la ciudad el título
        de Capital Cervantina de América por la UNESCO.
    </p>
    <p class="texto">
        Otros montajes del Teatro Universitario fueron Los pasos de Lope de Rueda en 1955, El
        retablillo jovial de Alejandro Casona en 1958, El caballero de Olmedo en 1962. Con motivo del
        primer Festival Internacional Cervantino, en 1972, Ruelas dirigió Las estampas del Quijote, de
        Salvador de Madariaga en la Plazuela de Cata y en 1979 presentó Dos hombres en la mina de
        Ferenc Herczeg, en el interior de la mina El nopal. Montaje que actualmente se sigue
        representando.
    </p>
</section>

<section class="contenedor2" id="contenedor2">
    <p class="titulo2">Compromiso con el Arte</p>
    <p class="texto2">
        Enrique Ruelas viajó semanalmente durante 35 años para cumplir con sus
        responsabilidades en Guanajuato, demostrando así su compromiso con el arte,
        mientras que en la UNAM se desempeñó como jefe del Departamento de Actividades
        Estéticas hasta su fallecimiento el 5 de octubre de 1987 en la Ciudad de México. Sus restos
        fueron llevados a Guanajuato, la tierra que tanto amó, ahí fue recibido con honores y su
        espíritu artístico seguirá inspirando a las futuras generaciones. Su vida fue una
        inspiración en la historia del teatro mexicano.
    </p>
</section>
